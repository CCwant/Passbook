package com.xinyue.passwd;

public class Content {
	
	private byte[] typeName;
	private byte[] text;
	
	public byte[] getTypeName() {
		return typeName;
	}
	public void setTypeName(byte[] typeName) {
		this.typeName = typeName;
	}
	public byte[] getText() {
		return text;
	}
	public void setText(byte[] text) {
		this.text = text;
	}

	
}
